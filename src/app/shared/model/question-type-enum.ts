/**
 * Kind of question, how questions will display in questionaire
 */
export enum QuestionTypeEnum {

  /**
   * Percent value, will display as slider from 0% to 100%
   */
  PercentSlider,

  /**
   * Single value, will display as Radio Group
   */
  RadioGroup,

  /**
   * Multiple values, will display as Checkbox Group
   */
  Checkbox
}

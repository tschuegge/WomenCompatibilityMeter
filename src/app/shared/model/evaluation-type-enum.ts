/**
 * Kind of evaluation for answer, how the user input is compared to the answer value
 */
export enum EvaluationTypeEnum {

  /**
   * User input is equal or less to answervalue
   */
  EqualOrLess,

  /**
   * User input is equal or more to answervalue
   */
  EqualOrMore,

  /**
   * User input is equal to answervalue
   */
  Equal
}
